const fs = require('fs');
const path = require('path');
const simpleGit = require('simple-git');

const REPO_URL = `https://${process.env.GITHUB_TOKEN}@github.com/CGMGMT/GymSync-Frontend.git`;
const REPO_DIR = './gymsync-frontend-temp';
const COMPONENT_PATH = 'src/components';

async function createFrontendComponent(componentName) {
  const git = simpleGit();

  try {
    // Clean up previous repo if it exists
    if (fs.existsSync(REPO_DIR)) {
      fs.rmSync(REPO_DIR, { recursive: true, force: true });
    }

    // Clone the frontend repo
    console.log(`ğŸŒ€ Cloning GymSync-Frontend...`);
    await git.clone(REPO_URL, REPO_DIR);
    console.log(`âœ… Clone complete.`);

    // Set up file and folder paths
    const componentDir = path.join(REPO_DIR, COMPONENT_PATH);
    const fileName = `${componentName}.tsx`;
    const filePath = path.join(componentDir, fileName);

    // Make sure the folder exists
    fs.mkdirSync(componentDir, { recursive: true });

    // Create the component file
    const componentCode = `
import React from 'react';

const ${componentName} = () => {
  return (
    <div>
      <h1>${componentName} Component</h1>
      <p>Generated by Autoflow ğŸš€</p>
    </div>
  );
};

export default ${componentName};
`;

    fs.writeFileSync(filePath, componentCode);
    console.log(`âœ… Component written to ${filePath}`);

    // Commit and push
    const repoGit = simpleGit(REPO_DIR);
    await repoGit.add('.');
    await repoGit.commit(`Autoflow: Created ${componentName} component`);

    console.log(`ğŸš€ Attempting push to GitHub...`);
    const pushResult = await repoGit.push('origin', 'main');
    console.log(`ğŸ“¦ Push result:`, pushResult);
    console.log(`âœ… Pushed ${componentName} to GitHub successfully.`);

  } catch (err) {
    console.error('âŒ Error during Autoflow push:', err);
  }
}

module.exports = { createFrontendComponent };
